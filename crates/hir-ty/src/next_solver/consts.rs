/// A type-level constant value.
///
/// Represents a typed, fully evaluated constant.
#[derive(Debug, Copy, Clone, Eq, PartialEq, Hash)]
pub struct ValueConst<'db> {
    pub(crate) ty: Ty<'db>,
    pub(crate) value: Valtree<'db>,
}

impl<'db> ValueConst<'db> {
    pub fn new(ty: Ty<'db>, scalar: ConstScalar) -> Self {
        let value = Valtree::new(scalar);
        ValueConst { ty, value }
    }
}

impl<'db> rustc_type_ir::inherent::ValueConst<DbInterner<'db>> for ValueConst<'db> {
    fn ty(self) -> Ty<'db> {
        self.ty
    }

    fn valtree(self) -> Valtree<'db> {
        self.value
    }
}

#[salsa::interned(constructor = new_)]
pub struct Valtree<'db> {
    #[return_ref]
    scalar_: ConstScalar,
}

impl<'db> Valtree<'db> {
    pub fn new(scalar: ConstScalar) -> Self {
        crate::next_solver::tls::with_db_out_of_thin_air(|db| unsafe {
            std::mem::transmute(Valtree::new_(db, scalar))
        })
    }

    pub fn inner(&self) -> &ConstScalar {
        // SAFETY: ¯\_(ツ)_/¯
        crate::next_solver::tls::with_db_out_of_thin_air(|db| {
            let inner = self.scalar_(db);
            unsafe { std::mem::transmute(inner) }
        })
    }
}

